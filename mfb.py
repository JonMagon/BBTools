import struct
import sys
from enum import Enum
from io import BytesIO
from PIL import Image

class EntryFlags(Enum):
    Transparent = 1
    Compressed = 2
    Unknown = 4

class MFBFile:
    def __init__(self, data, palette):
        self.entries = []
        stream = BytesIO(data)

        signature = stream.read(3).decode('ascii')
        if signature != 'MFB':
            raise Exception("File is corrupted. Bad signature.")

        version = int(stream.read(3).decode('ascii'))
        if version != 101:
            raise Exception("This version of MFB is not supported.")

        self.width = struct.unpack('<h', stream.read(2))[0]
        self.height = struct.unpack('<h', stream.read(2))[0]
        self.offset = (struct.unpack('<h', stream.read(2))[0], struct.unpack('<h', stream.read(2))[0])

        flags = struct.unpack('<h', stream.read(2))[0]
        numsprites = struct.unpack('<h', stream.read(2))[0]

        spritesize = self.width * self.height

        self.is_transparent = (flags & EntryFlags.Transparent.value) != 0
        self.is_unknown = (flags & EntryFlags.Unknown.value) != 0
        self.is_compressed = (flags & EntryFlags.Compressed.value) != 0

        if self.is_compressed:
            for _ in range(numsprites):
                size = struct.unpack('<i', stream.read(4))[0]
                self.entries.append(self.create_image(self.unpack_rle(stream.read(size), spritesize), palette))
        else:
            for _ in range(numsprites):
                self.entries.append(self.create_image(stream.read(spritesize), palette))

    @staticmethod
    def unpack_rle(data, spritesize):
        output = bytearray(spritesize)
        in_pos = 0
        out_pos = 0
        copy_byte = data[0]

        while out_pos < len(output):
            if data[in_pos] == copy_byte:
                count = data[in_pos + 1]
                in_pos += 2

                while count != 0:
                    output[out_pos] = copy_byte
                    out_pos += 1
                    count -= 1
            else:
                output[out_pos] = data[in_pos]
                out_pos += 1
                in_pos += 1

        return output

    def create_image(self, buffer, palette):
        img = Image.new('RGBA', (self.width, self.height))
        pixels = img.load()

        for y in range(self.height):
            for x in range(self.width):
                index = buffer[y * self.width + x]
                color = palette[index * 4: index * 4 + 4]
                pixels[x, y] = tuple(color)

        if self.is_transparent:
            transparent_color = pixels[0, 0]
            img = img.convert('RGBA')
            data = img.getdata()
            new_data = []
            for item in data:
                if item == transparent_color:
                    new_data.append((255, 255, 255, 0))
                else:
                    new_data.append(item)
            img.putdata(new_data)

        return img

